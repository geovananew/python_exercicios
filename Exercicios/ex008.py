#Escreva um programa que leia um valor em metros e o exiba convertido em centimetros e milímetros

# 1 metro = 100cm
# 1 metro = 1000 mm

m = float(input('Informe o metro: '))
cm = m * 100
mm= m * 1000
print(f'o valor em metros é {m}m, em centimetros é {cm}cm e em milimetros é {mm}mm')

